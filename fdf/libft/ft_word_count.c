#include "libft.h"

size_t	ft_word_count(char const *s, char c)
{
	size_t	count;

	count = 0;
	while (*s)
	{
		if (*s != c && (s == 0 || *(s - 1) == c))
			count++;
		s++;
	}
	return (count);
}
