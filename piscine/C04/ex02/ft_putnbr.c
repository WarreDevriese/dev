#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	long	x;
	char	buffer[11];

	i = 0;
	x = nb;
	if (x == 0)
		write(1, "0", 1);
	if (x < 0)
	{
		write(1, "-", 1);
		x = -x;
	}
	while (x > 0)
	{
		buffer[i++] = x % 10 + '0';
		x = x / 10;
	}
	while (i > 0)
		write(1, &buffer[--i], 1);
}
